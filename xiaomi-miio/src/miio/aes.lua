local Rcon = { 0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1b,0x36 }

local Nb = 4
local Nk = 4
local Nr = 10

local function sb(v) return sbox[v + 1] end
local function isb(v) return inv_sbox[v + 1] end

-- Multiply by x in GF(2^8) with reduction polynomial 0x11b.
local function xtime(b)
  return (((b << 1) & 0xFF) ~ (((b >> 7) & 1) * 0x1b)) & 0xFF
end

-- GF(2^8) multiplication. Used in mix-columns (small constants 0x09/0x0b/0x0d/0x0e).
local function gmul(a, b)
  local r = 0
  for _ = 1, 8 do
    if (b & 1) ~= 0 then r = r ~ a end
    a = xtime(a)
    b = b >> 1
  end
  return r & 0xFF
end

local function key_expansion(key)
  -- key: 16 raw bytes. Returns 11 round keys of 16 bytes each, indexed 1..11.
  local W = {}            -- 44 words of 4 bytes each, W[1..44]
  for i = 0, Nk - 1 do
    W[i + 1] = { key:byte(i*4 + 1), key:byte(i*4 + 2), key:byte(i*4 + 3), key:byte(i*4 + 4) }
  end
  for i = Nk, Nb * (Nr + 1) - 1 do
    local temp = { W[i][1], W[i][2], W[i][3], W[i][4] }
    if i % Nk == 0 then
      -- RotWord
      local t = temp[1]; temp[1] = temp[2]; temp[2] = temp[3]; temp[3] = temp[4]; temp[4] = t
      -- SubWord
      temp[1] = sb(temp[1]); temp[2] = sb(temp[2]); temp[3] = sb(temp[3]); temp[4] = sb(temp[4])
      temp[1] = temp[1] ~ Rcon[i // Nk]
    end
    local prev = W[i - Nk + 1]
    W[i + 1] = { prev[1] ~ temp[1], prev[2] ~ temp[2], prev[3] ~ temp[3], prev[4] ~ temp[4] }
  end
  -- Group W into 11 round keys (each 16 bytes, returned as 4x4 column-major state).
  local round_keys = {}
  for r = 0, Nr do
    local rk = {}
    for c = 0, 3 do
      local w = W[r * 4 + c + 1]
      rk[c * 4 + 1] = w[1]
      rk[c * 4 + 2] = w[2]
      rk[c * 4 + 3] = w[3]
      rk[c * 4 + 4] = w[4]
    end
    round_keys[r + 1] = rk
  end
  return round_keys
end

-- State is a flat array of 16 bytes laid out column-major:
--   s[1] s[5] s[9]  s[13]
--   s[2] s[6] s[10] s[14]
--   s[3] s[7] s[11] s[15]
--   s[4] s[8] s[12] s[16]

local function add_round_key(state, rk)
  for i = 1, 16 do state[i] = state[i] ~ rk[i] end
end

local function sub_bytes(state)
  for i = 1, 16 do state[i] = sb(state[i]) end
end

local function inv_sub_bytes(state)
  for i = 1, 16 do state[i] = isb(state[i]) end
end

local function shift_rows(state)
  -- Row 1 (indices 2,6,10,14): shift left 1
  local t = state[2]; state[2] = state[6]; state[6] = state[10]; state[10] = state[14]; state[14] = t
  -- Row 2 (indices 3,7,11,15): shift left 2
  t = state[3]; state[3] = state[11]; state[11] = t
  t = state[7]; state[7] = state[15]; state[15] = t
  -- Row 3 (indices 4,8,12,16): shift left 3 (== right 1)
  t = state[16]; state[16] = state[12]; state[12] = state[8]; state[8] = state[4]; state[4] = t
end

local function inv_shift_rows(state)
  -- Row 1: shift right 1
  local t = state[14]; state[14] = state[10]; state[10] = state[6]; state[6] = state[2]; state[2] = t
  -- Row 2: shift right 2 (same as left 2)
  t = state[3]; state[3] = state[11]; state[11] = t
  t = state[7]; state[7] = state[15]; state[15] = t
  -- Row 3: shift right 3 (== left 1)
  t = state[4]; state[4] = state[8]; state[8] = state[12]; state[12] = state[16]; state[16] = t
end

local function mix_columns(state)
  for c = 0, 3 do
    local i = c * 4
    local s0, s1, s2, s3 = state[i+1], state[i+2], state[i+3], state[i+4]
    local t = s0 ~ s1 ~ s2 ~ s3
    local u = s0
    state[i+1] = state[i+1] ~ t ~ xtime(s0 ~ s1)
    state[i+2] = state[i+2] ~ t ~ xtime(s1 ~ s2)
    state[i+3] = state[i+3] ~ t ~ xtime(s2 ~ s3)
    state[i+4] = state[i+4] ~ t ~ xtime(s3 ~ u)
  end
end

local function inv_mix_columns(state)
  for c = 0, 3 do
    local i = c * 4
    local s0, s1, s2, s3 = state[i+1], state[i+2], state[i+3], state[i+4]
    state[i+1] = gmul(s0, 0x0e) ~ gmul(s1, 0x0b) ~ gmul(s2, 0x0d) ~ gmul(s3, 0x09)
    state[i+2] = gmul(s0, 0x09) ~ gmul(s1, 0x0e) ~ gmul(s2, 0x0b) ~ gmul(s3, 0x0d)
    state[i+3] = gmul(s0, 0x0d) ~ gmul(s1, 0x09) ~ gmul(s2, 0x0e) ~ gmul(s3, 0x0b)
    state[i+4] = gmul(s0, 0x0b) ~ gmul(s1, 0x0d) ~ gmul(s2, 0x09) ~ gmul(s3, 0x0e)
  end
end

local function encrypt_block(state, round_keys)
  add_round_key(state, round_keys[1])
  for r = 2, Nr do
    sub_bytes(state)
    shift_rows(state)
    mix_columns(state)
    add_round_key(state, round_keys[r])
  end
  sub_bytes(state)
  shift_rows(state)
  add_round_key(state, round_keys[Nr + 1])
end

local function decrypt_block(state, round_keys)
  add_round_key(state, round_keys[Nr + 1])
  for r = Nr, 2, -1 do
    inv_shift_rows(state)
    inv_sub_bytes(state)
    add_round_key(state, round_keys[r])
    inv_mix_columns(state)
  end
  inv_shift_rows(state)
  inv_sub_bytes(state)
  add_round_key(state, round_keys[1])
end

local function block_from_string(s, off)
  local b = {}
  for i = 1, 16 do b[i] = s:byte(off + i - 1) end
  return b
end

local function block_to_string(b)
  return string.char(b[1],b[2],b[3],b[4],b[5],b[6],b[7],b[8],
                     b[9],b[10],b[11],b[12],b[13],b[14],b[15],b[16])
end

-- PKCS#7 padding: append N bytes of value N (N in 1..16).
local function pkcs7_pad(s)
  local pad = 16 - (#s % 16)
  return s .. string.rep(string.char(pad), pad)
end

local function pkcs7_unpad(s)
  if #s == 0 then return s end
  local pad = s:byte(#s)
  if pad < 1 or pad > 16 then return nil, "invalid padding length: " .. pad end
  for i = #s - pad + 1, #s do
    if s:byte(i) ~= pad then return nil, "invalid padding bytes" end
  end
  return s:sub(1, #s - pad)
end

function aes.encrypt_cbc(key, iv, plaintext)
  assert(#key == 16, "AES-128 key must be 16 bytes")
  assert(#iv == 16, "AES IV must be 16 bytes")
  local round_keys = key_expansion(key)
  local padded = pkcs7_pad(plaintext)
  local prev = block_from_string(iv, 1)
  local out = {}
  for off = 1, #padded, 16 do
    local blk = block_from_string(padded, off)
    for i = 1, 16 do blk[i] = blk[i] ~ prev[i] end
    encrypt_block(blk, round_keys)
    out[#out + 1] = block_to_string(blk)
    prev = blk
  end
  return table.concat(out)
end

function aes.decrypt_cbc(key, iv, ciphertext)
  assert(#key == 16, "AES-128 key must be 16 bytes")
  assert(#iv == 16, "AES IV must be 16 bytes")
  assert(#ciphertext > 0 and (#ciphertext % 16) == 0, "ciphertext must be non-empty multiple of 16")
  local round_keys = key_expansion(key)
  local prev = block_from_string(iv, 1)
  local out = {}
  for off = 1, #ciphertext, 16 do
    local cblk = block_from_string(ciphertext, off)
    local dblk = { table.unpack(cblk) }
    decrypt_block(dblk, round_keys)
    for i = 1, 16 do dblk[i] = dblk[i] ~ prev[i] end
    out[#out + 1] = block_to_string(dblk)
    prev = cblk
  end
  return pkcs7_unpad(table.concat(out))
end

return aes
